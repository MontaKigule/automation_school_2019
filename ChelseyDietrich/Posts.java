public class Posts {
     private int userId;
     private int id;
     private String title;
     private String body;

    public String getTitle() {
        return title;
    }

    public String getBody() {
        return body;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserId() {
        return userId;
    }
}
